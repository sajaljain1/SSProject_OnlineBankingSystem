package edu.asu.cse.softwaresecurity.group6.bosd.model.user;

public class SearchUserData {
	private String accountPermission;
	private String transactionPermission;
	private String firstName;
	private String lastName;
	private String creationTime;
	private String account1;
	private String account2;
	private String username;
	
	public String getAccountPermission() {
		return accountPermission;
	}
	public void setAccountPermission(String accountPermission) {
		this.accountPermission = accountPermission;
	}
	public String getTransactionPermission() {
		return transactionPermission;
	}
	public void setTransactionPermission(String transactionPermission) {
		this.transactionPermission = transactionPermission;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getCreationTime() {
		return creationTime;
	}
	public void setCreationTime(String creationTime) {
		this.creationTime = creationTime;
	}
	public String getAccount1() {
		return account1;
	}
	public void setAccount1(String account1) {
		this.account1 = account1;
	}
	public String getAccount2() {
		return account2;
	}
	public void setAccount2(String account2) {
		this.account2 = account2;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
}
